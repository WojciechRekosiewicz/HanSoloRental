package Model;

public class Vehicle {

    private String name;
    private String model;
    private int dateOfProduction;
    private int vehicleMileage;
    private String type;
    private int fuelAmount;
    private String isVehicleLend;


//Transporter, mysliwiec, walking, driving

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public int getDateOfProduction() {
        return dateOfProduction;
    }

    public void setDateOfProduction(int dateOfProduction) {
        this.dateOfProduction = dateOfProduction;
    }

    public int getVehicleMileage() {
        return vehicleMileage;
    }

    public void setVehicleMileage(int vehicleMileage) {
        this.vehicleMileage = vehicleMileage;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getFuelAmount() {
        return fuelAmount;
    }

    public void setFuelAmount(int fuelAmount) {
        this.fuelAmount = fuelAmount;
    }

    public String getIsVehicleLend() {
        return isVehicleLend;
    }

    public void setIsVehicleLend(String isVehicleLend) {
        this.isVehicleLend = isVehicleLend;
    }







}
